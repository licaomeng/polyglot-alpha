"""Background task that drains Polymarket fills into the on-chain router.

For each market we own:

  1. Poll :meth:`PolymarketV2Client.list_fills` every ``poll_interval``
     seconds.
  2. For every previously-unseen fill, call
     ``BuilderFeeRouter.recordFill(marketId, fillAmount, translator)``
     on Arc testnet via ``web3.py``.
  3. Emit an SSE-shaped dict to the configured sink so the FastAPI
     backend can fan it out to the UI.
  4. Persist a ``BuilderFeeEvent`` row through the supplied callback.

The contract address comes from env ``BUILDER_FEE_ROUTER_ADDRESS`` and
the signer from ``OPERATOR_WALLET_PRIVATE_KEY`` (we fall back to
``HACKATHON_WALLET_PRIVATE_KEY`` for local dev — the hackathon wallet
is the deployer of the router so it's already the operator). When
chain credentials are absent we still emit SSE + DB rows but skip the
``recordFill`` call and mark ``on_chain_status="skipped"``.
"""

from __future__ import annotations

import asyncio
import json
import logging
import os
import time
from pathlib import Path
from typing import Awaitable, Callable, Optional, Protocol

from polyglot_alpha.polymarket.types import BuilderFeeEvent, Fill

log = logging.getLogger(__name__)

DEFAULT_POLL_INTERVAL_SECONDS = 30
USDC_DECIMALS = 6  # both real and Arc-testnet MockUSDC use 6 decimals.

# Path to the compiled router ABI. Generated by `forge build`.
_DEFAULT_ROUTER_ABI_PATH = (
    Path(__file__).resolve().parents[2]
    / "contracts"
    / "out"
    / "BuilderFeeRouter.sol"
    / "BuilderFeeRouter.json"
)


class _FillSource(Protocol):
    async def list_fills(
        self, market_id: str, since_ts: int
    ) -> list[Fill]:  # pragma: no cover - structural
        ...


SSESink = Callable[[dict], Awaitable[None]]
DBSink = Callable[[BuilderFeeEvent], Awaitable[None]]


class FillListener:
    """Drains fills for a single (market_id, translator) pairing.

    Construct one per submission so each listener has its own polling
    cursor. Run with :meth:`listen` (forever, for production) or
    :meth:`poll_once` (for tests / single demo ticks).
    """

    def __init__(
        self,
        *,
        client: _FillSource,
        market_id: str,
        translator_address: str,
        poll_interval: int = DEFAULT_POLL_INTERVAL_SECONDS,
        sse_sink: Optional[SSESink] = None,
        db_sink: Optional[DBSink] = None,
        chain_recorder: Optional["ChainRecorder"] = None,
        time_fn: Callable[[], float] = time.time,
    ) -> None:
        if not market_id:
            raise ValueError("market_id is required")
        if not translator_address or not translator_address.startswith("0x"):
            raise ValueError("translator_address must be a 0x-prefixed EVM address")
        self._client = client
        self.market_id = market_id
        self.translator_address = translator_address
        self.poll_interval = poll_interval
        self._sse_sink = sse_sink
        self._db_sink = db_sink
        self._chain = chain_recorder
        self._time_fn = time_fn
        # Newest timestamp we've already drained. Start "now" so we
        # don't replay every historical fill on a cold start.
        self._cursor_ts: int = int(time_fn())
        self._seen_fill_ids: set[str] = set()
        self._stop = asyncio.Event()

    # ----- public ops ----------------------------------------------------

    async def poll_once(self) -> list[BuilderFeeEvent]:
        """One polling tick — process all new fills and return the events."""
        fills = await self._client.list_fills(self.market_id, self._cursor_ts)
        events: list[BuilderFeeEvent] = []
        for fill in fills:
            if fill.fill_id in self._seen_fill_ids:
                continue
            self._seen_fill_ids.add(fill.fill_id)
            event = await self._process_fill(fill)
            events.append(event)
            if fill.timestamp > self._cursor_ts:
                self._cursor_ts = fill.timestamp
        return events

    async def listen(self) -> None:
        """Poll forever until :meth:`stop` is called."""
        log.info(
            "FillListener starting market=%s translator=%s interval=%ss",
            self.market_id,
            self.translator_address,
            self.poll_interval,
        )
        while not self._stop.is_set():
            try:
                await self.poll_once()
            except Exception:  # noqa: BLE001 — never let a poll kill the loop
                log.exception(
                    "FillListener poll failed for market=%s", self.market_id
                )
            try:
                await asyncio.wait_for(
                    self._stop.wait(), timeout=self.poll_interval
                )
            except asyncio.TimeoutError:
                continue

    def stop(self) -> None:
        self._stop.set()

    # ----- internals -----------------------------------------------------

    async def _process_fill(self, fill: Fill) -> BuilderFeeEvent:
        event = BuilderFeeEvent(
            fill_id=fill.fill_id,
            market_id=fill.market_id,
            translator_address=self.translator_address,
            fill_amount_usdc=fill.fill_amount_usdc,
            builder_fee_usdc=fill.builder_fee_usdc,
            is_simulated=fill.is_simulated,
            timestamp=fill.timestamp,
        )

        if self._chain is not None:
            try:
                tx_hash = await self._chain.record_fill(
                    market_id=fill.market_id,
                    fill_amount_usdc=fill.builder_fee_usdc,
                    translator_address=self.translator_address,
                )
                event.tx_hash = tx_hash
                event.on_chain_status = "confirmed" if tx_hash else "skipped"
            except Exception as exc:  # noqa: BLE001
                log.exception(
                    "recordFill on chain failed for fill=%s", fill.fill_id
                )
                event.on_chain_status = "failed"
                event.tx_hash = None
                event = event.model_copy(update={"on_chain_status": "failed"})
                # Keep going — we still want the SSE + DB row so the UI
                # can flag the failure.
                _ = exc
        else:
            event.on_chain_status = "skipped"

        if self._db_sink is not None:
            try:
                await self._db_sink(event)
            except Exception:  # noqa: BLE001
                log.exception("DB sink failed for fill=%s", fill.fill_id)

        if self._sse_sink is not None:
            try:
                await self._sse_sink(
                    {
                        "type": "builder_fee.accrued",
                        "data": event.model_dump(),
                    }
                )
            except Exception:  # noqa: BLE001
                log.exception("SSE sink failed for fill=%s", fill.fill_id)

        return event


