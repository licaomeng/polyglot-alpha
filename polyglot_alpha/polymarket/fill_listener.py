class ChainRecorder:
    """Wraps ``BuilderFeeRouter.recordFill`` calls via web3.py.

    Kept in this module to keep the polymarket package self-contained;
    nothing else in the codebase needs it. Constructing one is cheap and
    safe even when the chain creds are missing — it will simply refuse
    to send and return ``None`` from :meth:`record_fill`.
    """

    def __init__(
        self,
        *,
        rpc_url: Optional[str] = None,
        router_address: Optional[str] = None,
        private_key: Optional[str] = None,
        abi_path: Optional[Path] = None,
        chain_id: Optional[int] = None,
    ) -> None:
        self.rpc_url = rpc_url or os.getenv("ARC_TESTNET_RPC")
        self.router_address = router_address or os.getenv(
            "BUILDER_FEE_ROUTER_ADDRESS"
        )
        self.private_key = private_key or os.getenv(
            "OPERATOR_WALLET_PRIVATE_KEY"
        ) or os.getenv("HACKATHON_WALLET_PRIVATE_KEY")
        raw_chain = chain_id or os.getenv("ARC_CHAIN_ID")
        self.chain_id = int(raw_chain) if raw_chain else None
        self._abi_path = abi_path or _DEFAULT_ROUTER_ABI_PATH
        self._w3 = None
        self._contract = None
        self._account = None

    @property
    def enabled(self) -> bool:
        return bool(
            self.rpc_url
            and self.router_address
            and self.private_key
            and self._abi_path.exists()
        )

    def _lazy_init(self) -> bool:
        """Defer web3 import until we actually need it (keeps tests fast)."""
        if self._contract is not None:
            return True
        if not self.enabled:
            return False
        try:
            from eth_account import Account  # type: ignore
            from web3 import Web3  # type: ignore
        except ImportError:  # pragma: no cover - web3 is in requirements
            log.warning("web3 not installed; ChainRecorder disabled")
            return False

        with self._abi_path.open("r", encoding="utf-8") as fh:
            artifact = json.load(fh)
        abi = artifact.get("abi", artifact)

        self._w3 = Web3(Web3.HTTPProvider(self.rpc_url))
        self._account = Account.from_key(self.private_key)
        self._contract = self._w3.eth.contract(
            address=Web3.to_checksum_address(self.router_address), abi=abi
        )
        return True

    async def record_fill(
        self,
        *,
        market_id: str,
        fill_amount_usdc: float,
        translator_address: str,
    ) -> Optional[str]:
        """Send the on-chain transaction and return the tx hash hex.

        Returns ``None`` when disabled (missing env / ABI) so callers
        can label the event "skipped" instead of "failed".
        """
        if not self._lazy_init():
            return None
        # web3 calls are blocking — push them to a thread to keep the
        # asyncio loop responsive.
        return await asyncio.to_thread(
            self._send_record_fill_sync,
            market_id,
            fill_amount_usdc,
            translator_address,
        )

    def _send_record_fill_sync(
        self,
        market_id: str,
        fill_amount_usdc: float,
        translator_address: str,
    ) -> str:
        from web3 import Web3  # type: ignore

        assert self._w3 is not None and self._contract is not None
        amount_base = int(round(fill_amount_usdc * (10**USDC_DECIMALS)))
        if amount_base <= 0:
            raise ValueError("fill_amount_usdc must be > 0 (after scaling)")

        translator = Web3.to_checksum_address(translator_address)
        nonce = self._w3.eth.get_transaction_count(self._account.address)
        tx = self._contract.functions.recordFill(
            market_id, amount_base, translator
        ).build_transaction(
            {
                "from": self._account.address,
                "nonce": nonce,
                "chainId": self.chain_id
                or self._w3.eth.chain_id,
                "gas": 250_000,
                "gasPrice": self._w3.eth.gas_price,
            }
        )
        signed = self._account.sign_transaction(tx)
        tx_hash = self._w3.eth.send_raw_transaction(signed.raw_transaction)
        return tx_hash.hex()
